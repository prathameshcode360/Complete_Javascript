// Variables and Data Types

// Declaring variables
var x = 5; // var
let y = 10; // let
const z = 15; // const

// Data types
let string = "Hello, World!"; // String
let number = 123; // Number
let boolean = true; // Boolean
let array = [1, 2, 3, 4, 5]; // Array
let object = { name: "John", age: 30 }; // Object

console.log(x, y, z, string, number, boolean, array, object);
